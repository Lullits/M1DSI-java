
public class Sabonete {

		String cor;
		String cheiro;
		
		public Sabonete(String cor, String cheiro) { //construtor que chama a variavel direto
			super();
			this.cor = cor;
			this.cheiro = cheiro;
		}
		@Override
		public String toString() {
			return "Sabonete [cor=" + cor + ", cheiro=" + cheiro + "]\n";
		}		
		
		
		
	}


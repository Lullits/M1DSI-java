
public class Calculadora {

	public static void main(String[] args) {
		double n1 = 5;
		double n2 = 2;
		double rsom;
		double rsub;
		double rmul;
		double rdv;
//declarando as variaveis de respostas como double e dando os valores diretamente dos números.
		
		
		rsom = n1 + n2; //conta da soma
		System.out.println("O resultado da soma é: " + rsom);

		rsub = n1 - n2; //conta da subtração
		System.out.println("O resultado da subtração é: " + rsub);
		
		rmul = n1 * n2; // conta da multiplicação
		System.out.println("O resultado da multiplicação é: " + rmul);
		
		rdv = n1 / n2; //conta da divisão
		System.out.println("O resultado da divisão é: " + rdv);
		
		

	}

}
